package sample.application.rap;

public class MathOperations {
	static double sum;
	public double add(double a, double b) {
		sum = a+b;
		return sum;
	}
	public double sub(double a, double b) {
		sum = a-b;
		return sum;
	}
	public double mul(double a, double b) {
		sum = a*b;
		return sum;
	}
	public double div(double a, double b) {
		sum = a/b;
		return sum;
	}
	
	
	
	
	
}
